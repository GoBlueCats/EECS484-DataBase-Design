CREATE TABLE Boats(bid INTEGER PRIMARY KEY, bname VARCHAR(30), color VARCHAR(10));


CREATE TABLE Reserves(sid INTEGER, bid INTEGER, rday VARCHAR(10),
       FOREIGN KEY (sid) REFERENCES Sailors,
       FOREIGN KEY (bid) REFERENCES Boats
       );


